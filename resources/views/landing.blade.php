<h1>Flores medicinales</h1>
<p>
    <li>LAVANDA: Son muy variados: alivia la ansiedad y el estrés, mejora el estado de ánimo, aclara el acné, etc.<br>
        <p>
    <li> CLAVEL:
        Cuando se separan de la base de la flor (que es amarga), los pétalos se pueden preparar para hacer un té
        excelente para reducir la ansiedad, la agitación, el estrés y la fatiga.</li>
<P>
    <li>CALÉNDULA:
        Posee propiedades antiinflamatorias, antifúngicas y antimicrobianas. Es la gran planta medicinal para el cuidado
        de la piel, por lo que se incluye en muchas pomadas y cremas.
    </li>
<p>
    <li>
        EQUINÁCEA:
        Destaca su efecto estimulante sobre el sistema inmunitario que ayuda a prevenir y tratar infecciones víricas del
        sistema respiratorio.
        Posee compuestos que han demostrado ayudar a aliviar la ansiedad, reducir la inflamación y mejorar el acné y el
        eccema.
    </li>
<p>
    <li>
        MARGARITA:
        Esta flor tiene una gran cantidad de propiedades medicinales a pesar de su apariencia sencilla. Cuando se
        consume en infusión, actúa como laxante y expectorante.
        También se utiliza como remedio casero para ayudar a tratar trastornos como la artritis.
    </li>
<p>
    <li>
        CRISANTEMO:
        Los crisantemos pueden sumergirse en agua caliente durante 4 minutos para preparar una infusión de delicioso
        sabor.
        Beberla alivia la fiebre, el dolor de cabeza y el resfriado común. </li>
<p>
    <li>
        JAZMÍN:

        Las dulces flores de jazmín no solo hacen una deliciosa taza de té, sino que también ayudan en problemas
        digestivos y úlceras estomacales.
        Beber esta infusión antes de acostarse puede ayudar a evitar el insomnio y la ansiedad. </li>
